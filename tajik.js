// ── Определение языка текста ─────────────────────
function detectLanguage(text) {
  if (!text) return 'unknown';

  const tjChars = /[ҷҲҳӣӢқҚғҒ]/;
  const tjWords = /\b(ММД|таваррум|сомонӣ|содирот|воридот|аҳолӣ|буҷет|дурнамо|рушд|сармоя)\b/i;
  const ruChars = /[ёЁыЫъЪ]/;

  const hasTJ = tjChars.test(text) || tjWords.test(text);
  const hasRU = ruChars.test(text) || /\b(ВВП|рубль|Россия|министерство)\b/.test(text);

  if (hasTJ && !hasRU) return 'tj';
  if (hasRU && !hasTJ) return 'ru';
  if (hasTJ && hasRU) return 'tj';
  if (/[а-яА-Я]/.test(text)) return 'tj_latin_cyr';
  return 'ru';
}

// ── Нормализация таджикского текста ─────────────
function normalizeTajik(text) {
  if (!text) return text;
  let result = text.toLowerCase();
  for (const [tj, ru] of Object.entries(TJ_TO_RU)) {
    result = result.replace(new RegExp(tj.toLowerCase(), 'gi'), ru);
  }
  return result;
}

// ── Перевод UI элементов ─────────────────────────
function translate(text, lang = 'ru') {
  if (lang === 'ru') return text;
  if (lang === 'tj') return RU_TO_TJ[text] || text;
  return text;
}

