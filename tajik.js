// ── Экстракция чисел из таджикского текста ──────
function extractTajikNumbers(text) {
  const numbers = [];
  const patterns = [
    /(\d[\d\s,.]*)[\s]*млн[\s]*сомонӣ/gi,
    /(\d[\d\s,.]*)[\s]*млрд[\s]*сомонӣ/gi,
    /(\d[\d\s,.]*)[\s]*млн[\s]*доллар/gi,
    /(\d[\d\s,.]*)[\s]*фоиз/gi,
    /(\d[\d\s,.]*)[\s]*ҳазор/gi,
  ];
  patterns.forEach(pattern => {
    let match;
    while ((match = pattern.exec(text)) !== null) {
      numbers.push({
        raw:   match[0],
        value: parseFloat(match[1].replace(/[\s,]/g, '')),
        unit:  match[0].replace(match[1], '').trim(),
      });
    }
  });
  return numbers;
}

// ── Анализ таджикского документа ────────────────
function analyzeTajikDocument(text) {
  const lang       = detectLanguage(text);
  const numbers    = extractTajikNumbers(text);
  const normalized = normalizeTajik(text);
  const foundTerms = Object.keys(TJ_TO_RU).filter(term =>
    text.toLowerCase().includes(term.toLowerCase())
  );
  return {
    language:           lang,
    foundTerms,
    numbers,
    normalizedText:     normalized,
    hasEconomicContent: foundTerms.length > 2,
  };
}

